    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
    //ACTUALIZAR
        try {
            // Captura de datos
            int id = Integer.parseInt(txtId.getText());
            String nombre = txtNombre.getText().trim();
            double precioBase = Double.parseDouble(txtPrecioBase.getText().trim());
            double tamanoMB = Double.parseDouble(txtTamaño.getText().trim());
            int numeroPaginas = Integer.parseInt(txtNumeroPaginas.getText());
            String formato = (String) cmbFormato.getSelectedItem();

            // Creamos la instancia actualizada
            Ebook ebookActualizado = new Ebook(id, nombre, precioBase, tamanoMB, numeroPaginas, formato);

            // Llamamos al servicio
            boolean exito = ProductoService.actualizarProducto(ebookActualizado);

            if (exito) {
                javax.swing.JOptionPane.showMessageDialog(this, "Ebook actualizado correctamente");
                this.dispose(); 
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: No se encontró el producto con ID: " + id);
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Verifique que los campos numéricos sean correctos.");
        } catch (Exception ex) {
            Logger.getLogger(ViewEbook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

