    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
    //ELIMINAR
    // 1. Obtener el ID del campo de texto
        int id = Integer.parseInt(txtId.getText());

        // 2. Mostrar cuadro de diálogo para confirmar si está segura
        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(
            this, 
            "¿Estás segura de que deseas eliminar este producto?", 
            "Confirmar Eliminación", 
            javax.swing.JOptionPane.YES_NO_OPTION,
            javax.swing.JOptionPane.WARNING_MESSAGE
        );

        // 3. Si el usuario presiona "SÍ"
        if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
            // Llamamos al método eliminar de tu ProductoService
            boolean eliminado = com.mycompany.service.ProductoService.eliminar(id);

            if (eliminado) {
                javax.swing.JOptionPane.showMessageDialog(this, "Producto eliminado correctamente.");
                this.dispose(); // Cerramos la ventana después de eliminar
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: No se pudo eliminar el producto.");
            }
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

